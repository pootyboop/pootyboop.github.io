function makePlatformRepresentation(platform, i, searchTerm) {
    var ret = "";
    var highlight = false;
    
    if (searchTerm != "") {
        if (platform.toLowerCase().includes(searchTerm)) {
            highlight = true;
        }
    }

    switch (platform) {

        case "Mac":
        case "Windows":
        case "Android":
        case "Web":
        case "VR":
        case "Quest":
        case "Spotify":
        case "Apple Music":
        case "YouTube":
            ret = makePlatformImg(platform, highlight);
            break;

        default:
            if (i > 0) {
                ret = ", ";
            }

            ret += platform;
    }

    return ret;
}

